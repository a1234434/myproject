package com.java2.io;

public class PracticeSales {
	int rank;
	int amount;

	public PracticeSales(int amount, int rank) {
		super();
		this.amount = amount;
		this.rank = rank;
	}

	public int getrank() {
		return rank;
	}

	public void setrank(int rank) {
		this.rank = rank;
	}

	public int getamount() {
		return amount;
	}

	public void setamount(int amount) {
		this.amount = amount;
	}
}
